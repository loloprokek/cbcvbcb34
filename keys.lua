{
    "KEY_ADMIN": {
        "banned": false,
        "expires": 0,
        "note": "Мой админский ключ"
    },
    "KEY_USER_1": {
        "banned": false,
        "expires": 0,
        "note": "Ключ для друга"
    },
    "KEY_BANNED": {
        "banned": true,
        "expires": 0,
        "note": "Забанен за слив"
    }
}

* **Важно:** Кавычки `""` обязательны. Запятые `,` между блоками обязательны. Последний блок запятой не заканчивается.
* **expires:** Если поставить `0` — ключ вечный. Если хочешь время, используй [Unix Timestamp](https://www.unixtimestamp.com/) (например, `1735689600` — это 1 января 2025 года).
